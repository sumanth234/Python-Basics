

numbers = [1,2,3,4,5,6,7]

print(numbers[:]) # makes a new copy of numbers and prints

print(numbers[1:]) # prints numbers from index 1 till end

print(numbers[1:5]) # prints numbers from index 1 till index 5

print(numbers[-2:]) # prints numbers from index length-2 till end

print(numbers[:-3]) #prints numbers from index 0 till index length - 3

print(numbers[-3:-2]) #prints numbers from index length -3 to length -2